import {mkdirSync, writeFileSync} from 'node:fs';
import {dirname, join} from 'node:path';
import {fileURLToPath} from 'node:url';
import {formatCompactDownloads} from './format-compact-downloads.mjs';

const PACKAGE = 'poker-calculations';
const RANGE_START = '2026-01-01';
const FALLBACK_FORMATTED = '1.6k';
const FALLBACK_RAW = 1600;

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT_FILE = join(root, 'src/generated/npm-downloads.ts');

function yesterdayUTC() {
  const d = new Date();
  d.setUTCDate(d.getUTCDate() - 1);
  return d.toISOString().slice(0, 10);
}

/**
 * @param {{formatted: string; raw: number; rangeStart: string; rangeEnd: string; fetchedAt: string}} data
 */
function writeGeneratedTs(data) {
  mkdirSync(dirname(OUT_FILE), {recursive: true});
  const lines = [
    '// Auto-generated by scripts/fetch-npm-downloads.mjs — do not edit.',
    `export const npmDownloadsFormatted = ${JSON.stringify(data.formatted)};`,
    `export const npmDownloadsRaw = ${data.raw};`,
    `export const npmDownloadsRangeStart = ${JSON.stringify(data.rangeStart)};`,
    `export const npmDownloadsRangeEnd = ${JSON.stringify(data.rangeEnd)};`,
    `export const npmDownloadsFetchedAt = ${JSON.stringify(data.fetchedAt)};`,
    '',
  ];
  writeFileSync(OUT_FILE, lines.join('\n'), 'utf8');
}

function writeFallback(reason) {
  const rangeEnd = yesterdayUTC();
  console.warn(`fetch-npm-downloads: using fallback (${reason})`);
  writeGeneratedTs({
    formatted: FALLBACK_FORMATTED,
    raw: FALLBACK_RAW,
    rangeStart: RANGE_START,
    rangeEnd,
    fetchedAt: new Date().toISOString(),
  });
}

/**
 * @returns {Promise<{formatted: string; raw: number; rangeStart: string; rangeEnd: string; fetchedAt: string}>}
 */
async function fetchNpmDownloads() {
  const rangeEnd = yesterdayUTC();
  const url = `https://api.npmjs.org/downloads/range/${RANGE_START}:${rangeEnd}/${PACKAGE}`;
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`npm downloads API ${res.status}`);
  }
  const body = await res.json();
  if (!Array.isArray(body?.downloads)) {
    throw new Error('unexpected npm API shape');
  }
  const raw = body.downloads.reduce((sum, day) => sum + day.downloads, 0);
  return {
    formatted: formatCompactDownloads(raw),
    raw,
    rangeStart: RANGE_START,
    rangeEnd,
    fetchedAt: new Date().toISOString(),
  };
}

try {
  const data = await fetchNpmDownloads();
  writeGeneratedTs(data);
  console.log(
    `npm downloads: ${data.formatted} (${data.raw}) ${data.rangeStart}..${data.rangeEnd}`,
  );
} catch (err) {
  const message = err instanceof Error ? err.message : String(err);
  writeFallback(message);
}

process.exit(0);
